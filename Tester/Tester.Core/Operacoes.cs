using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Tester.Core
{
    public static class Operacoes
    {
        public static double Somar(double primeiroNumero, double
        segundoNumero)
        {
            return (primeiroNumero + segundoNumero);
        }
        public static double Multiplicar(double primeiroNumero,
        double segundoNumero)
        {
            return (primeiroNumero * segundoNumero);
        }
    }
}
